# Print is a built-in Function
 
# Print Function is used to show output

# Inside a 'print' function, there can be variable 
    #or any data types
    
# A print function has 2 optional parameters - 
    # end = '' {used to separate two 'print'}
    # sep = '' {used to separate two arguments}
    
# Print function has a 'f' String Syntax
    # which is the shorter form of 'format' method

# Below is the syntax :

# It will show the output
print('Hello World')

# End is a parameter which separates two print functions
# by the given value
print(23443, end='\n')

print(243.3423)

a = 'mujahid al mahi'
print(a)

# f string:
name = 'Mahi'
age = 17
print(f'My name is {name}. I am {age} years old')

# This sep parameter separates two different origin of code by the given value
print('Hey', 'I love Python so much!', sep='|')

